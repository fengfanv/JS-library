// 互斥锁
// 互斥锁指当事件被触发后，立即执行核心代码，如果核心代码没有执行完，事件又被触发，则不进行任何处理，简单点说就是同一时间只能做一件事情。
// 互斥锁应用场景：如果事件被频繁触发，互斥锁能保证核心代码在同一时间只能被执行一次 ！如：可以使用互斥锁技术实现下拉加载查看更多功能。
