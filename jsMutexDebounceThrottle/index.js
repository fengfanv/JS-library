// 节流阀
// 节流是指连续触发某个事件，但在单位时间内只执行一次。和互斥锁有点像，互斥锁是等任务处理完，函数才可以再次被使用。这里节流，不管任务做没做完，到时间了，函数就可以再次被使用。
// 节流应用场景：如果事件被频繁触发，节流能够减少事件触发的频率，因此，节流是有选择性地执行一部分事件！如：可以使用节流优化鼠标跟随效果。

let isRun = true;
function doSomething(){
    if(!isRun){
        return false;
    }

    // 真正要执行的核心代码或调用核心函数
    // code...
    isRun = false;

    setTimeout(()=>{
        isRun = true;
    },1000)
}

